/**
 * Start an indirect navigation.
 * If there exist an indirect navigation that does not yet have an endTime. end it, then create a new one.
 */
NLRUM.markIndirectStart = function () {
	// Update marks
	var curTime = Date.now();
	NLRUM.mark("indirectnavigationstarttime", curTime);

	// When there is an unfinished indirect navigation,
	// terminate the previous indirect navigation to start a new one.
	// We do not change anything about the hard navigaiton entry or completed soft navigation.
	if ('soft' === NLRUM.pendingBrowserEntry.linkType && !NLRUM.pendingBrowserEntry.done)
	{
		NLRUM.pendingBrowserEntry.done = curTime;
		NLRUM.pendingBrowserEntry.status = NLRUM.status.attempted;
	}

	// Update the previous soft navigation entry's resourceMatchEndTime if necessary.
	if ('soft' === NLRUM.pendingBrowserEntry.linkType && NLRUM.pendingBrowserEntry.resourceMatchEndTime > curTime)
	{
		NLRUM.pendingBrowserEntry.resourceMatchEndTime = curTime;
	}

	// Map all the resources prior to the new navigation
	NLRUM.resourceMapping();

	NLRUM.completedBrowserEntries.push(Object.assign({}, NLRUM.pendingBrowserEntry));

	//create a new soft navigation browser entry.
	NLRUM.pendingBrowserEntry = NLRUM.getBrowsertimeEntry('soft');
	NLRUM.sendDataWithIncreasedInterval();
};

