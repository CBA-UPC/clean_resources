NLRUM.processClientScriptTime = function(trigger, scriptid, args, startTime, executionTime, recordType, version, isAsyncCallback) {
	function getZeroBasedIndex(idx) {
		if (isNaN(idx)) {
			return idx;
		}
		else {
			idx = parseInt(idx, 10);
			return (idx < 0) ? idx : idx - 1;
		}
	}
	var perfData = {};

	switch (trigger) {
		case "pageInit":
			perfData["mode"] = args[0];
			break;
		case "lineInit":
		case "validateDelete":
		case "validateInsert":
		case "validateLine":
			perfData["sublistId"] = args[0];
			break;
		case "recalc":
		case "sublistChanged": //recalc in 2.0
			perfData["sublistId"] = args[0];
			perfData["operation"] = args[1];
			break;
		case "postSourcing":
			perfData["sublistId"] = args[0];
			perfData["fieldId"] = args[1];
			perfData["line"] = version === '1.0' ? args[2] : getZeroBasedIndex(args[2]);
			break;
		case "validateField":
		case "fieldChanged":
			perfData["sublistId"] = args[0];
			perfData["fieldId"] = args[1];
			perfData["line"] = version === '1.0' ? args[2] : getZeroBasedIndex(args[2]);
			perfData["column"] = version === '1.0' ? args[3] : getZeroBasedIndex(args[3]);
			break;
		case "saveRecord":
			break;
		default:
			return;
	}
	perfData["trigger"] = trigger;
	perfData["scriptId"] = scriptid;
	perfData["startTime"] = startTime;
	perfData["executionTime"] = executionTime;
	perfData["recordType"] = recordType;
	perfData["runtimeVersion"] = version;
	perfData["url"] = window.location.pathname;
	perfData["sid"] = NLRUM.sessionId;
	perfData["isAsyncCallback"] = isAsyncCallback;

	if (NLRUM.isFirstLoad) {
		perfData["threadId"] = sessionStorage.getItem('nthreadid');
		perfData["sOperationId"] = sessionStorage.getItem('soperationid2') || sessionStorage.getItem('soperationid');
	}

	NLRUM.clientScriptTime.push(perfData);

	if (NLRUM.clientScriptTime.length >= 50) {
		NLRUM.sendData(NLRUM.beaconUrl, {"clientscripttime" : NLRUM.clientScriptTime});
		NLRUM.clientScriptTime = [];
	}
};

// Measure an episode.
NLRUM.measure = function(episodeName, startNameOrTime, endNameOrTime) {
	NLRUM.dprint("NLRUM.measure: " + episodeName + ", " + startNameOrTime + ", " + endNameOrTime);

	if ( ! episodeName) {
		NLRUM.dprint("Error: episodeName is undefined in NLRUM.measure.");
		return;
	}

	var startEpochTime;
	if ( "undefined" === typeof(startNameOrTime) ) {
		if ( "number" === typeof(NLRUM.marks[episodeName]) ) {
			// If no startName is specified, then use the episodeName as the start mark.
			startEpochTime = NLRUM.marks[episodeName];
		}
		else {
			// Create a "measure" that is this exact point in time?
			startEpochTime = new Date().getTime();
		}
	}
	else if ( "number" === typeof(NLRUM.marks[startNameOrTime]) ) {
		// If a mark with this name exists, use that.
		startEpochTime = NLRUM.marks[startNameOrTime];
	}
	else if ( "number" === typeof(startNameOrTime) ) {
		// Assume a specific epoch time is provided.
		startEpochTime = startNameOrTime;
	}
	else {
		NLRUM.dprint("Error: unexpected startNameOrTime in NLRUM.measure: " + startNameOrTime);
		return;
	}

	var endEpochTime;
	if ( "undefined" === typeof(endNameOrTime) ) {
		endEpochTime = new Date().getTime();
	}
	else if ( "number" === typeof(NLRUM.marks[endNameOrTime]) ) {
		// If a mark with this name exists, use that.
		endEpochTime = NLRUM.marks[endNameOrTime];
	}
	else if ( "number" === typeof(endNameOrTime) ) {
		endEpochTime = endNameOrTime;
	}
	else {
		NLRUM.dprint("Error: unexpected endNameOrTime in NLRUM.measure: " + endNameOrTime);
		return;
	}

	NLRUM.starts[episodeName] = parseInt(startEpochTime, 10);
	NLRUM.measures[episodeName] = parseInt(endEpochTime - startEpochTime, 10);

	if ( NLRUM.bPostMessage ) {
		window.postMessage("NLRUM:measure:" + episodeName + ":" + startEpochTime + ":" + endEpochTime, NLRUM.targetOrigin);
	}
};

