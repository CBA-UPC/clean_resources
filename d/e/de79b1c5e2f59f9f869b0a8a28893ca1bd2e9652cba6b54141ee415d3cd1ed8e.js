NLRUM.getLoggingThreshold = function() {
	function getTelemetrySetting(settingName) {
		var telemetrySettingsCookie = /(^| )telemetrySettings=([^;]+)/.exec(document.cookie);
		if (telemetrySettingsCookie)
		{
			var telemetrySettings = {}, n;
			var rx = /(\w+):([^|]*)/g;
			while (n = rx.exec(telemetrySettingsCookie)) {
				telemetrySettings[n[1]] = unescape(n[2]);
			}
			if (telemetrySettings[settingName])
			{
				return telemetrySettings[settingName];
			}
		}
		return "";
	}

	function isOverrideInEffect() {
		var overrideStartTime = getTelemetrySetting("loggingOverrideStartTime");
		var overrideEndTime = getTelemetrySetting("loggingOverrideEndTime");
		if (NLRUM.validateClientScriptThreshold(overrideStartTime) && NLRUM.validateClientScriptThreshold(overrideEndTime)) {
			var currentTime = new Date().getTime();
			return (currentTime >= overrideStartTime) && (currentTime <= overrideEndTime);
		} else {
			return false;
		}
	}

	function getMinClientScriptDuration() {
		if (isOverrideInEffect()) {
			var minClientScriptDuration = getTelemetrySetting("minClientScriptDuration");
			if (NLRUM.validateClientScriptThreshold(minClientScriptDuration)) {
				return minClientScriptDuration;
			}
		}
		return NLRUM.minClientScriptDuration;
	}

	return getMinClientScriptDuration();
};

