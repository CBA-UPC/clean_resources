/**
 * For any action that started over the defined time limit:
 * End the action to be prepped for sending.
 * Loop in reverse order to prevent splice error
 * @param timeLimit - limit for how long an action can exist in a pending state.
 */
NLRUM.endPendingActionOverTimeLimit = function(timeLimit)
{
	var current = Date.now();
	for (var i = NLRUM.pendingActions.length - 1; i >= 0; i--)
	{
		if (NLRUM.pendingActions[i].startTime < current - timeLimit)
		{
			NLRUM.actionEnd(NLRUM.pendingActions[i].actionId, {status: NLRUM.status.attempted}, false);
			NLRUM.completedActionEntries.push(NLRUM.actionEntries.pop());
		}
	}
};

NLRUM.sendResTiming = function(resources) {
	if (NLRUM.bResourceTimingDataCollection > 0) {
		var jsonParams = {'resourcetime' : resources || NLRUM.getResourceTiming()};
		if (jsonParams.resourcetime.length !== 0) {
			NLRUM.sendData(NLRUM.beaconUrl, jsonParams);
		}
	}
};

NLRUM.onRestimingBufferFull = function() {
	if (NLRUM.bResourceTimingDataCollection > 0)
	{
		if (NLRUM.pendingActions.length > 0 || NLRUM.actionEntries.length > 0 || (Object.keys(NLRUM.pendingBrowserEntry).length > 0 && NLRUM.pendingBrowserEntry.resourceMatchEndTime && NLRUM.pendingBrowserEntry.resourceMatchEndTime > Date.now()))
		{
			NLRUM.resourceMapping();
		}
		else
		{
			NLRUM.sendResTiming();
			NLRUM.clearResourceTimings(true);
		}
	}
};

// Process any commands in the queue.
// The command queue is used to store calls to the API before the full script has been loaded.
NLRUM.processQ = function() {
	var len = NLRUM.q.length;
	for ( var i = 0; i < len; i++ ) {
		var aParams = NLRUM.q[i];
		var cmd = aParams[0];
		if ( "mark" === cmd ) {
			NLRUM.mark(aParams[1], aParams[2]);
		}
		else if ( "measure" === cmd ) {
			NLRUM.measure(aParams[1], aParams[2], aParams[3]);
		}
		else if ( "done" === cmd ) {
			NLRUM.done(aParams[1]);
		}
	}
};

// Set a time marker (typically the beginning of an episode).
NLRUM.mark = function(markName, markTime) {
	NLRUM.dprint("NLRUM.mark: " + markName + ", " + markTime);

	if ( ! markName) {
		NLRUM.dprint("Error: markName is undefined in NLRUM.mark.");
		return;
	}

	NLRUM.marks[markName] = parseInt(markTime || new Date().getTime());

	if ( NLRUM.bPostMessage ) {
		window.postMessage("NLRUM:mark:" + markName + ":" + markTime, NLRUM.targetOrigin);
	}
};

