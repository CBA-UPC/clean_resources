// Find the start time based on a cookie set by NLRUM in the unload handler.
NLRUM.findStartCookie = function() {
	var aCookies = document.cookie.split(' ');
	for ( var i = 0; i < aCookies.length; i++ ) {
		if ( 0 === aCookies[i].indexOf("NLRUM=") ) {
			var aSubCookies = aCookies[i].substring("NLRUM=".length).split('&');
			var startTime, bReferrerMatch;
			for ( var j = 0; j < aSubCookies.length; j++ ) {
				if ( 0 === aSubCookies[j].indexOf("s=") ) {
					startTime = aSubCookies[j].substring(2);
				}
				else if ( 0 === aSubCookies[j].indexOf("r=") ) {
					var startPage = aSubCookies[j].substring(2);
					bReferrerMatch = ( escape(document.referrer) == startPage );
				}
			}
			if ( bReferrerMatch && startTime ) {
				NLRUM.dprint("NLRUM.findStartCookie: startTime = " + startTime);
				return startTime;
			}
		}
	}
	return undefined;
};

NLRUM.beforeUnload = function(e) {
	var jsonParams = {};

	//Client Script
	if (NLRUM.clientScriptTime.length !== 0) {
		jsonParams['clientscripttime'] = NLRUM.clientScriptTime;
		NLRUM.clientScriptTime = [];
	}

	if (Object.keys(NLRUM.pendingBrowserEntry).length > 0) {  // if not NLCORP
		//Cancel pending action entries
		for (var i = 0; i < NLRUM.pendingActions.length; i++) {
			NLRUM.actionEnd(NLRUM.pendingActions[i].actionId, {status: NLRUM.status.attempted}, true);
		}
		NLRUM.resourceMapping();
		NLRUM.pendingActions = [];

		//Completed pending browser session entry
		if ('soft' === NLRUM.pendingBrowserEntry.linkType && !NLRUM.pendingBrowserEntry.done) {
			NLRUM.pendingBrowserEntry.done = Date.now();
			NLRUM.pendingBrowserEntry.status = NLRUM.status.attempted;
		}
		NLRUM.completedBrowserEntries.push(Object.assign({}, NLRUM.pendingBrowserEntry));
		NLRUM.pendingBrowserEntry = {};

		//Cancel pending flow entry
		NLRUM.flowEnd({status : NLRUM.status.attempted}, true);

		jsonParams['flowEntries'] = NLRUM.flowEntries;
		jsonParams['actionEntries'] = NLRUM.actionEntries.concat(NLRUM.completedActionEntries);
		jsonParams['browsertime'] = NLRUM.completedBrowserEntries;
		NLRUM.flowEntries = [];
		NLRUM.actionEntries = [];
		NLRUM.completedActionEntries = [];
		NLRUM.completedBrowserEntries = [];
	}

	//ResourceTime
	var resrcTimingArray = NLRUM.getResourceTiming();
	if (resrcTimingArray.length > 0 || NLRUM.unmatchedResources.length > 0) {
		jsonParams['resourcetime'] = resrcTimingArray.concat(NLRUM.unmatchedResources);
	}

	//Use Beacon API to send data before unloaded, to prevent missing data collection
	jsonParams['beaconSentTime'] = new Date().getTime();
	if (navigator.sendBeacon) {
		navigator.sendBeacon(NLRUM.beaconUrl, JSON.stringify(jsonParams));
	}
	else {
		var xmlHttpReq = (window.XMLHttpRequest) ? new XMLHttpRequest() : (window.ActiveXObject) ? new ActiveXObject("Microsoft.XMLHTTP") : null;
		if (xmlHttpReq) {
			xmlHttpReq.open('POST', NLRUM.beaconUrl, false);
			xmlHttpReq.setRequestHeader('Content-Type', 'application/json');
			xmlHttpReq.setRequestHeader('X-Supress-Cookie-JSessionID', 'true');
			xmlHttpReq.send(JSON.stringify(jsonParams));
		}
	}
};

