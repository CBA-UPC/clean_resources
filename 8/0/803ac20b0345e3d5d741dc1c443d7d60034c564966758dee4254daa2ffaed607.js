function dateFormater(date) {
  const d = new Date(date);  // 2009-11-10
  const month = d.toLocaleString('default', { month: 'long' });
  date_new = month+" "+('0' + d.getDate()).slice(-2)+", "+d.getFullYear() ;
  return date_new;
}

