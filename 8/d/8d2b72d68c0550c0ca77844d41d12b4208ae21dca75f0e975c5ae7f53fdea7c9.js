        function animBackgroundIntro6(){
            var RAD = Math.PI / 180;
            var PI_2 = Math.PI / 2;
            var clipPath = document.querySelector("#arcPath");
            var clipPath2 = document.querySelector("#arcPath2");
            var arc = {
                start: 0,
                end: 0,
                cx: 539,
                cy: 40,
                r: 90
            };
            var arc2 = {
                start: 0,
                end: 0,
                cx: 537,
                cy: 48,
                r: 80
            };
            updatePath();
            updatePath2();

            function updatePath(end) {
                clipPath.setAttribute("d", getPath(arc.cx, arc.cy, arc.r, arc.start, end));
            }

            function updatePath2(end) {
                clipPath2.setAttribute("d", getPath(arc2.cx, arc2.cy, arc2.r, arc2.start, end));
            }

            function getPath(cx, cy, r, a1, a2) {
                var delta = a2 - a1;
                if (delta === 360) {
                    return "M " + (cx - r) + " " + cy + " a " + r + " " + r + " 0 1 0 " + r * 2 + " 0 a " + r + " " + r + " 0 1 0 " + -r * 2 + " 0z";
                }
                var largeArc = delta > 180 ? 1 : 0;
                a1 = a1 * RAD - PI_2;
                a2 = a2 * RAD - PI_2;
                var x1 = cx + r * Math.cos(a2);
                var y1 = cy + r * Math.sin(a2);
                var x2 = cx + r * Math.cos(a1);
                var y2 = cy + r * Math.sin(a1);
                return "M " + x1 + " " + y1 + " A " + r + " " + r + " 0 " + largeArc + " 0 " + x2 + " " + y2 + " L " + cx + " " + cy + "z";
            } 
            let debut, tempsPrecedent;
            let fini = false;
            function iteration(chrono) {
                if (debut === undefined) {
                    debut = chrono;
                }
                let ecoule = chrono - debut;
                if (tempsPrecedent !== chrono) {
                    let compteur = Math.min((360/2500) * ecoule, 360);
                    updatePath(compteur);
                    updatePath2(compteur);
                    if (compteur === 360) fini = true;
                }
                if (ecoule < 2500) {
                    tempsPrecedent = chrono;
                    if (!fini) window.requestAnimationFrame(iteration);                    
                }
            }
            window.requestAnimationFrame(iteration);
        }
